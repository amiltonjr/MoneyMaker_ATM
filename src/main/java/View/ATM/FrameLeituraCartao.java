    public void clearInstance() {
        try { 
            ContaDAO.getInstance().getEntity().clear();
            
            this.contas = ContaDAO.getInstance().findAll();

            cmbClientes.removeAllItems();
            cmbClientes.addItem("Selecione uma conta");

            for (Conta c : this.contas) {
                cmbClientes.addItem("AG: " + c.getAgencia() + " CONTA Nº " + c.getNumeroConta() + " CLIENTE: " + c.getCliente().getNome());
            }
        } catch (Exception e) {}
    }
    
    public Conta getConta(int index) {
        return contas.get(index);
    }

    public void recarregarClientes() {

        clearInstance();
        contas = ContaDAO.getInstance().findAll();

        cmbClientes.removeAllItems();
        cmbClientes.addItem("Selecione uma conta");

        for (Conta c : this.contas) {
            cmbClientes.addItem("AG: " + c.getAgencia() + " CONTA Nº " + c.getNumeroConta() + " CLIENTE: " + c.getCliente().getNome());
        }
    }
    
    

