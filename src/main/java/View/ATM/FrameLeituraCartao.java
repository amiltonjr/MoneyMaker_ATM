    private void btnInserirCartaoCliked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnInserirCartaoCliked

        int index = cmbClientes.getSelectedIndex();

        if (index > 0) {
            // Chama a ação na classe ControlLeituraCartao
            ControlLeituraCartao.simularLeitura(this, getNextFrame(), contas.get(index-1));

        } else {
            JOptionPane.showMessageDialog(new JFrame(), "Selecione um cliente!", "Erro", JOptionPane.ERROR_MESSAGE);
            cmbClientes.requestFocus();
        }
    }//GEN-LAST:event_btnInserirCartaoCliked

